package net.ymate.module.wechat.base;

import com.alibaba.fastjson.JSONObject;

/**
 * @author 刘镇 (suninformation@163.com) on 16/6/1 上午1:27
 * @version 1.0
 */
public class WechatMassVideo {

    private String mediaId;

    private String title;

    private String description;

    public String getMediaId() {
        return mediaId;
    }

    public void setMediaId(String mediaId) {
        this.mediaId = mediaId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String toJSON() {
        JSONObject _json = new JSONObject();
        _json.put("media_id", this.getMediaId());
        _json.put("title", this.getTitle());
        _json.put("description", this.getDescription());
        return _json.toString();
    }
}
